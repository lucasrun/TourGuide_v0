public class CustomData {

    private int mIcon, mText;

    public CustomData(int nIcon, int nText) {
        mIcon = nIcon;
        mText = nText;
    }

    public int getIcon() {
        return mIcon;
    }

    public int getText() {
        return mText;
    }
}




